package ejercicio3;

public class Ejercicio3 {

	public static void main(String[] args) {
		
	
		int numero = 100;
		
		if (numero >= 1)
			do {
            System.out.print(numero + " ");
            numero--;
			} while (numero >= 1);
		
		
	}

}
